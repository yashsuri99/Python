## For - Used to iterate over a range of values or a sequence one by one. Executed for items in range. Sequence can be a list, string, tuple

# Range function - Used to create a list containing sequence of number starting from the start to one less than the end

x = range(1,10)

for i in x:
    print(i)

## While - Repeatedly executes the set of statements till the defined condition is true. As soon as false it exits the loop

i = 1

while(i<10):
    print(i)
    i+=1
